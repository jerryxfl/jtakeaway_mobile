package com.jerry.jtakeaway.bean.model;

public class Axis {
    private float X;
    private float Y;

    public Axis() {
    }

    public Axis(float x, float y) {
        X = x;
        Y = y;
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }
}
